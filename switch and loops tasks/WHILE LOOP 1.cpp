#include <iostream>
using namespace std;

int main()
{
    int i = 2;   // Start from 2

    // Loop runs while i is less than or equal to 20
    while(i <= 20)
    {
        cout << i << " ";
        i += 2;   // Increase by 2 to get next even number
    }

    return 0;
}
