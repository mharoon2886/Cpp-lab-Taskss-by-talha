#include <iostream>   // This library is used for input and output
using namespace std;

int main()
{
    int i = 1;   // Start the variable i with value 1

    // while loop will run while the condition is true
    while(i <= 10)
    {
        cout << i << endl;   // Print the value of i on a new line
        i++;                 // Increase the value of i by 1
    }

    return 0;   // Program ends
}
