#include<iostream>
using namespace std;

int main() {
    int a = 5;        // Declare an integer variable 'a' and assign 5
    float b = 2.16;   // Declare a float variable 'b' and assign 2.16
    double c = 8.5423; // Declare a double variable 'c' and assign 8.5423

    // Print value, size, and memory address of 'a'
    cout << "value of a " << a << endl;
    cout << "size of a " << sizeof(a) << endl;
    cout << "location of a " << &a << endl;

    // Print value, size, and memory address of 'b'
    cout << "value of b " << b << endl;
    cout << "size of b " << sizeof(b) << endl;
    cout << "location of b " << &b << endl;

    // Print value, size, and memory address of 'c'
    cout << "value of c " << c << endl;
    cout << "size of c " << sizeof(c) << endl;
    cout << "location of c " << &c << endl;

    return 0;  // End of program
} 


